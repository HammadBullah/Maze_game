  void movePlayer(int newRow, int newCol) {
    if (newRow >= 0 &&
        newRow < levels[currentLevel].length &&
        newCol >= 0 &&
        newCol < levels[currentLevel][0].length &&
        levels[currentLevel][newRow][newCol] != 1) {
      playerRow = newRow;
      playerCol = newCol;
      if (levels[currentLevel][playerRow][playerCol] == 2) {
        if (currentLevel < levels.length - 1) {
          currentLevel++;
          playerRow = 1;
          playerCol = 1;
        }
      }
    }
  }
